"""Пример локали Adalight: английский язык.

Локаль — это язык интерфейса, поставляемый как плагин («язык — тоже мод»).
Файл кладётся в папку плагинов рядом с обычными плагинами, но вместо
create_plugin() экспортирует create_locale().

Как установить:
1. Скопируйте файл в папку плагинов (кнопка «Открыть папку плагинов»
   в менеджере плагинов).
2. Перезапустите Adalight.
3. Выберите язык в «Система → Язык».

Контракт локали:
- модуль экспортирует create_locale() -> объект с атрибутами:
  code — код языка (например "en"),
  name — название языка на нём самом ("English"),
  translations — словарь {русская строка из интерфейса: перевод}.
- Ключи — это в точности русские строки, обёрнутые в коде в i18n.tr().
  Неизвестный ключ откатывается к русскому исходнику, так что переводить
  можно постепенно.
"""

