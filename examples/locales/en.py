class EnglishLocale:
    code = "en"
    name = "English"
    translations = {
        # сайдбар
        "Режим": "Mode",
        "Устройство": "Device",
        "Изображение": "Image",
        "Яркость": "Brightness",
        "Плагины": "Plugins",
        "Система": "System",
        # статус-карточка и управление
        "Старт": "Start",
        "Стоп": "Stop",
        "Остановлено": "Stopped",
        "Нажмите «Старт», чтобы включить подсветку": "Press “Start” to turn the lighting on",
        "Ночной режим": "Night mode",
        "Применить сейчас": "Apply now",
        # калибровка
        "Калибровка": "Calibration",
        "Стороны": "Sides",
        "Бегущий диод": "Running dot",
        "Погасить": "Turn off",
        "Сохранить настройки": "Save settings",
        # система
        "Язык": "Language",
        "О программе": "About",
        "Сообщить об ошибке": "Report a bug",
        "Предложить идею": "Suggest an idea",
        "Открыть менеджер плагинов…": "Open plugin manager…",
    }


def create_locale() -> EnglishLocale:
    return EnglishLocale()
